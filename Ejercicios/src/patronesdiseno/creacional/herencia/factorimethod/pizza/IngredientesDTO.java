package patronesdiseno.creacional.herencia.factorimethod.pizza;

public class IngredientesDTO {
	
	private Integer id;
	private String masa;
	private String queso;
	private String tomate;
	private String oregano;
	private String nombre;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMasa() {
		return masa;
	}
	public void setMasa(String masa) {
		this.masa = masa;
	}
	public String getQueso() {
		return queso;
	}
	public void setQueso(String queso) {
		this.queso = queso;
	}
	public String getTomate() {
		return tomate;
	}
	public void setTomate(String tomate) {
		this.tomate = tomate;
	}
	public String getOregano() {
		return oregano;
	}
	public void setOregano(String oregano) {
		this.oregano = oregano;
	}
	
	
	
}
